package Model;

/**
 * Represents the four cardinal directions.
 * This enum is used to specify movement or orientation
 * within the application's model.
 *
 * @author Husein
 */
public enum Direction {
    /**
     * The direction pointing towards the North.
     */
    NORTH,

    /**
     * The direction pointing towards the East.
     */
    EAST,

    /**
     * The direction pointing towards the South.
     */
    SOUTH,

    /**
     * The direction pointing towards the West.
     */
    WEST;
}